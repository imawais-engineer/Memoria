export function MediaTypeIcon({ type, className = '' }) {
  if (type === 'image') return <IconImage className={className} />
  if (type === 'video') return <IconVideo className={className} />
  if (type === 'voice' || type === 'audio') return <IconVoice className={className} />
  return <MemoriaLogoMark className={className} />
}

